package com.supplier;

public class SupplierBean {
	private int companyId;/*供应商编号*/
	private String companyName;/*供应商名称*/
	private String companyShort;/*简称*/
	private String companyAddress;/*公司地址*/
	private String manager;/*负责人*/
	private String companyTel;/*联系电话*/
	private String email;/*邮箱地址*/
	private String postalCode;/*邮政编码*/
	private String fax;/*传真*/
	private String bankName;/*开户银行*/
	private String bankAccounts;/*银行账户*/
	private String addTime;/*最后修改时间*/
	private String remarks;/*备注*/
	public int getCompanyId() {
		return companyId;
	}
	public void setCompanyId(int companyId) {
		this.companyId = companyId;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getCompanyShort() {
		return companyShort;
	}
	public void setCompanyShort(String companyShort) {
		this.companyShort = companyShort;
	}
	public String getCompanyAddress() {
		return companyAddress;
	}
	public void setCompanyAddress(String companyAddress) {
		this.companyAddress = companyAddress;
	}
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}
	public String getCompanyTel() {
		return companyTel;
	}
	public void setCompanyTel(String companyTel) {
		this.companyTel = companyTel;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPostalCode() {
		return postalCode;
	}
	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getBankAccounts() {
		return bankAccounts;
	}
	public void setBankAccounts(String bankAccounts) {
		this.bankAccounts = bankAccounts;
	}
	public String getAddTime() {
		return addTime;
	}
	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

}
