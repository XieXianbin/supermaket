package com.SaleRank;

public class SaleRankBean {

private int proId;//商品编号
private int count;//销售时间
private String time;//一次销售的数量

public int getProId() {
	return proId;
}
public void setProId(int proId) {
	this.proId = proId;
}
public int getCount() {
	return count;
}
public void setCount(int count) {
	this.count = count;
}
public String getTime() {
	return time;
}
public void setTime(String time) {
	this.time = time;
}

}
