package com.turnover;

public class TurnoverBean {
	
	private String time;
	private float money;
	private int count;
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public float getMoney() {
		return money;
	}
	public void setMoney(float monry) {
		this.money = monry;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}

}
